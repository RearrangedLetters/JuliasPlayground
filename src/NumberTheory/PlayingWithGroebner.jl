md"""
Here is how to find roots of multivariate polynomials using some advanced techniques implemented in HomotopyContinuation.jl
"""

# ╔═╡ f15e351c-27ad-4fb7-86c8-b84ab5344eb5
@var a b

# ╔═╡ c5c32265-dd91-413e-b3bf-eac179e0fb23
f = (a^4 + b^4 - 1) * (a^2 + b^2 - 2) + a^5 * b

# ╔═╡ d33dd01a-a4cd-4e51-aec7-54156b61acaf
normalform(basis, f)

# ╔═╡ 30fcd0eb-e5c4-40b6-8cfd-75602514300a
@var u[1:2] λ

# ╔═╡ d52984ed-a3ae-4ab1-a6e7-342b0225cd2b
∇f = differentiate(f, [a,b])

# ╔═╡ 0546a404-0641-468c-984c-f90a05f5c813
C = System([[a, b] - u - ∇f .* λ; f]; variables = [a,b,λ], parameters = u)

# ╔═╡ a4c768c4-9f07-494a-9c4f-44b565b7b0d2
hsolve = HomotopyContinuation.solve

# ╔═╡ 1a70932e-46b3-40af-ad4a-284050ca6e64
find_start_pair(C; max_tries = 100, atol = 0.0, rtol = 1e-12)

# ╔═╡ 3c3f07db-39e9-496d-bad7-285776032903
result = @time monodromy_solve(C)

